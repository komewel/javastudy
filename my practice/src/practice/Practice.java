package practice;

public class Practice {
	
	private String name;
	private int age;
	
	public Practice() {} //이건 뭔 역할 하는건지 
	public Practice(String name, int age) {
		this.name = name;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	

}

	

